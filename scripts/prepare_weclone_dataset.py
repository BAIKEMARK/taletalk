#!/usr/bin/env python3
"""Prepare a WeClone/LLaMA Factory ShareGPT dataset from generated role data."""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Iterable


ROLE_MAP = {
    "human": "user",
    "user": "user",
    "gpt": "assistant",
    "assistant": "assistant",
}


def load_json(path: Path) -> list[dict]:
    data = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(data, list):
        raise ValueError(f"expected a JSON list: {path}")
    return data


def convert_sharegpt_rows(rows: Iterable[dict]) -> list[dict]:
    converted: list[dict] = []
    for row in rows:
        source_messages = row.get("messages") or row.get("conversations")
        if not isinstance(source_messages, list):
            raise ValueError("each row must contain messages or conversations")

        messages = []
        for msg in source_messages:
            source_role = msg.get("role", msg.get("from"))
            content = msg.get("content", msg.get("value"))
            role = ROLE_MAP.get(source_role)
            if role is None or not isinstance(content, str) or not content.strip():
                raise ValueError(f"invalid message: {msg!r}")
            messages.append({"role": role, "content": content.strip()})

        if len(messages) < 2 or messages[0]["role"] != "user" or messages[-1]["role"] != "assistant":
            raise ValueError("each row must start with a user message and end with an assistant message")

        converted.append({"system": str(row.get("system", "")).strip(), "messages": messages})
    return converted


def build_dataset_info(dataset_name: str, file_name: str = "./sft-my.json") -> dict:
    return {
        dataset_name: {
            "file_name": file_name,
            "formatting": "sharegpt",
            "columns": {"messages": "messages", "system": "system"},
            "tags": {
                "role_tag": "role",
                "content_tag": "content",
                "user_tag": "user",
                "assistant_tag": "assistant",
            },
        }
    }


def write_weclone_dataset(
    rows: Iterable[dict],
    out_dir: Path,
    dataset_name: str = "chat-sft",
    limit: int | None = None,
) -> dict:
    source_rows = list(rows)
    converted = convert_sharegpt_rows(source_rows)
    if limit is not None:
        converted = converted[:limit]

    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "sft-my.json").write_text(
        json.dumps(converted, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    (out_dir / "dataset_info.json").write_text(
        json.dumps(build_dataset_info(dataset_name), ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    stats = {
        "source_examples": len(source_rows),
        "written_examples": len(converted),
        "dataset_name": dataset_name,
        "sft_path": str(out_dir / "sft-my.json"),
        "dataset_info_path": str(out_dir / "dataset_info.json"),
    }
    (out_dir / "stats.json").write_text(json.dumps(stats, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return stats


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--source", type=Path, required=True, help="ShareGPT JSON generated by extract-dialogue")
    parser.add_argument("--out-dir", type=Path, required=True, help="Output directory for sft-my.json")
    parser.add_argument("--dataset-name", default="chat-sft")
    parser.add_argument("--limit", type=int, default=None, help="Write only the first N examples")
    args = parser.parse_args()

    stats = write_weclone_dataset(
        load_json(args.source),
        args.out_dir,
        dataset_name=args.dataset_name,
        limit=args.limit,
    )
    print(json.dumps(stats, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
